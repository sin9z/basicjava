class  Loops
{
	public static void main(String[] args) 
	{
		for (int i= 1;i<= 10 ;i++ )
		{
			System.out.print(i+" ");
		}
		for (char ch= 'a';ch<= 'z' ;ch++ )
		{
			System.out.print(ch+" ");
		}
		for (char ch= 'A';ch<= 'Z' ;ch++ )
		{
			System.out.print(ch+" ");
		}
			for (char ch= '1';ch<= '9' ;ch++ )
		{
			System.out.print(ch+" ");
		}
       	for (int i= 1;i<= 127 ;i++ )
		{
			System.out.println(i+" : "+((char)i));
		}
		
		
		
		
		
	}
}
